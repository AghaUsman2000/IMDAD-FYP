class PostEntity {
  final int accepted;
  final String description;
  final double lat;
  final double long;
  final String nid;
  final String quantity;
  final String title;
  final String uid;
  final String name;
  final String number;

  PostEntity({
    required this.accepted,
    required this.description,
    required this.lat,
    required this.long,
    required this.nid,
    required this.quantity,
    required this.title,
    required this.uid,
    required this.name,
    required this.number,
  });
}
